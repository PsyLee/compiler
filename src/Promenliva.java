public class Promenliva {
    
    String ime;
    String vrednost;

    public Promenliva(String ime, String vrednost) {
        this.ime = ime;
        this.vrednost = vrednost;
    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getVrednost() {
        return vrednost;
    }

    public void setVrednost(String vrednost) {
        this.vrednost = vrednost;
    }
    
    
    
}
